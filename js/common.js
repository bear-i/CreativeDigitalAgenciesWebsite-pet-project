$(document).ready(function() {

  $('.menu-trigger').click(function(e){
      e.preventDefault();
      $('.hidden-menu').slideToggle(500);
  });

  $(window).resize(function(){
    if($(window).width()>992){
      $('.hidden-menu').removeAttr('style');
    }
  });

   $(".menu a").click(function(e){
        e.preventDefault();
        $(this).parents("ul").find("li.active").removeClass("active");
        $(this).parent().addClass("active");
    });

    $(".hidden-menu li").click(function(e){
        e.preventDefault();
        $(this).parents("ul").find("li.hidden-active").removeClass("hidden-active");
        $(this).addClass("hidden-active");
    });

    $(".scroll").click(function(e){
    e.preventDefault();
    var link=$(this).attr('href');
    $("html, body").animate({
      scrollTop: $(link).offset().top
    },1000);
  });

    $(".scroll").mouseover(function(){
      $(".slogan figcaption").css("color", "#11749e");
    });

    $(".scroll").mouseout(function(){
      $(".slogan figcaption").css("color","white");
    });

  $("input[type='tel").mask("+38 (999) 999 99 99");
});

